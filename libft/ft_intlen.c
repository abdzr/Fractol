#include "libft.h"

size_t	ft_intlen(int n)
{
	int cntnmbr;

	cntnmbr = 0;
	if (n < 0)
		n = n * 10;
	while (n)
	{
		n = n / 10;
		cntnmbr++;
	}
	return (cntnmbr);
}
