#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	j;
	size_t			re;

	i = 0;
	while (dst[i])
		i++;
	re = 0;
	while (src[re])
		re++;
	if (size <= i)
		re = re + size;
	else
		re = re + i;
	j = 0;
	while (src[j] && (i + 1) < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (re);
}
