#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*dst;

	i = 0;
	dst = (char *)b;
	while (len > i)
	{
		dst[i] = c;
		i++;
	}
	return ((void *)b);
}
